/**
 * Creates a translation function for the given language.
 * @param {string} lang - Language code ('en' or 'ar')
 * @returns {function} t(key, params?) - Looks up a translation key and interpolates params
 */
function createT(lang) {
  const primary = translations[lang] || translations.en;
  const fallback = translations.en;

  return function t(key, params) {
    let str = primary[key] ?? fallback[key] ?? key;

    if (params) {
      for (const [name, value] of Object.entries(params)) {
        str = str.replace(new RegExp(`\\{${name}\\}`, "g"), value);
      }
    }

    return str;
  };
}

